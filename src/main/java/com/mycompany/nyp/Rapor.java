    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        String ad = jComboBox1.getSelectedItem().toString();
        for (Cocuk cocuk : Aadmin.getCocuklar()) {
            System.out.println(cocuk);
            if (cocuk.getName().equals(ad)) {
                for (Test test : cocuk.getTestler()) {
                    if (test.getBaslama_zamani() != null) {
                        System.out.println(test);
                        dataset.setValue(test.getDogru_sayisi(), "Doğru Sayısı", test.getName());
                        dataset.setValue(test.getYanlis_sayisi(), "Yanlış Sayısı", test.getName());
                    } else {
                        System.out.println("TEST YOK!");
                    }
                }
            } else {
                System.out.println("İSİM YOK!");
                System.out.println(cocuk.getName());
                System.out.println(ad);
            }
        }

        JFreeChart chart = ChartFactory.createBarChart(
                "Doğru/Yanlış", "Test Name", "Doğru/Yanlış", dataset,
                PlotOrientation.VERTICAL, false, true, false
        );
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.black);
        ChartPanel chartPanel = new ChartPanel(chart);
        jDesktopPane1.add(chartPanel);
        chartPanel.setBounds(0, 0, jDesktopPane1.getWidth(), jDesktopPane1.getHeight());


    }//GEN-LAST:event_jButton2MouseClicked

