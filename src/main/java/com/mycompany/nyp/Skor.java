    public Skor(Cocuk cocuk, String s) {
        initComponents();
        this.s = s;
        this.aCocuk = cocuk;
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();

        for (Test test : aCocuk.testler) {
            if (test.getDogru_sayisi() + test.getYanlis_sayisi() == test.getSoru_sayisi()) {
                skor.add(test.getSkor());
            }
        }
        System.out.println(skor);
        Collections.sort(skor, Collections.reverseOrder());
        for (int i = 0; i < skor.size(); i++) {
            for (Test test : aCocuk.testler) {
                if (test.getSkor() == skor.get(i)) { 
                    Object[] rowData = {test.getName(), test.getSure_skor(), test.getSkor()};
                    tableModel.addRow(rowData);
                }
            }
        }
    }

